function onKeyDown(event) {
  if (isTransitioning) return;

  if (event.key === "Escape") {
    if (fullscreenViewer.classList.contains("is-open")) {
      closeFullscreenViewer();
      return;
    }
    if (app.classList.contains("is-project")) {
      closeProject();
    } else if (app.classList.contains("is-about")) {
      toggleAbout();
    } else if (app.classList.contains("is-home-preview")) {
      exitPreview();
    }
  }

  if (event.key === "Enter" && !app.classList.contains("is-project")) {
    openProject(activeIndex);
  }

  if (event.key === "ArrowRight") {
    if (app.classList.contains("is-project")) {
      const currentProject = projects[openedIndex];
      if (hasImageSequence(currentProject) && !hasProjectVideo(currentProject)) {
        renderFrame(frameIndex + 1);
      }
      return;
    } else {
      scrollTarget = clampHomeScroll(scrollTarget + gap);
    }
  }

  if (event.key === "ArrowLeft") {
    if (app.classList.contains("is-project")) {
      const currentProject = projects[openedIndex];
      if (hasImageSequence(currentProject) && !hasProjectVideo(currentProject)) {
        renderFrame(frameIndex - 1);
      }
      return;
    } else {
      scrollTarget = clampHomeScroll(scrollTarget - gap);
    }
  }
}

